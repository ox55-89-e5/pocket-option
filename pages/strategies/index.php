<p>Новички часто ищут универсальный подход, который будет приносить прибыль в любой ситуации. Опытные трейдеры понимают: рынок меняется, и стратегии нужно подбирать под текущие условия.</p>

<h2>Трендовые стратегии</h2>
<h3>Пробитие уровней</h3>
<p>Когда цена пробивает значимый уровень поддержки или сопротивления, движение часто продолжается.</p>
<ul>
  <li>Ждём, пока цена дважды коснётся одной отметки, формируя уровень</li>
  <li>Следим за пробоем — свеча должна закрыться за уровнем</li>
  <li>Входим на следующей свече в сторону пробоя</li>
  <li>Экспирацию ставим на 15–30 минут</li>
</ul>

<h3>Скользящие средние</h3>
<p>Две скользящие средние с периодами 10 и 30. Когда быстрая линия пересекает медленную снизу вверх — сигнал на покупку. Сверху вниз — на продажу.</p>

<h2>Контртрендовые стратегии</h2>
<h3>Отскок от уровня</h3>
<p>Если цена подходит к сильному уровню поддержки или сопротивления и появляется разворотная свеча (пин-бар, поглощение), входим в сторону отскока.</p>

<h3>Дивергенция по MACD и RSI</h3>
<p>Когда цена обновляет максимум, а индикатор показывает более низкий пик — это дивергенция, сигнал разворота.</p>

<h2>Price Action и свечной анализ</h2>
<ul>
  <li><strong>Пин-бар</strong> — свеча с длинной тенью и маленьким телом. Указывает на разворот.</li>
  <li><strong>Поглощение</strong> — вторая свеча полностью перекрывает тело предыдущей.</li>
  <li><strong>Внутренний бар</strong> — свеча внутри диапазона предыдущей. Предшествует сильному движению.</li>
</ul>

<h2>Как выбрать стратегию под себя</h2>
